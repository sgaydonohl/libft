#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char *s_ptr;

	if (!n)
		return ;
	s_ptr = (char *)s;
	while (n > 0)
	{
		*s_ptr++ = 0;
		n--;
	}
}
